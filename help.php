				<article id='welcome'>
					<h2>Benvenuto!</h2>
					<p>Ciao, hai bisogno di aiuto? Qui trovi tutto quello che ti serve per utilizzare al meglio il nostro servizio.</p>
					<p>Scegli una voce qui a sinistra!</p>
				</article>
				<article id='homePageArticle'>
					<h2>Pagina iniziale</h2>
					<p>La pagina iniziale è il punto di inizio della tua esperienza di navigazione. Contiene tutti gli articoli che sono stati pubblicati, e le categorie in cui essi sono raccolti. </p>
					<p>In alto, subito a destra del nome del sito, la casella di ricerca permette di cercare tra gli articoli in base a delle parole chiave. La ricerca è effettuata sul titolo, sul testo e sui tag associati all'articolo.</p> 
					<p>La piccola icona circolare accanto alla casella di testo ti permetterà di effettuare l'accesso o di registrarti al nostro servizio.</p>
					<p>Subito sotto, il vero centro del blog: gli articoli.</p>
				</article>
				<article id='postArticle'>
					<h2>Articoli</h2>
					<p>Per pubblicare un articolo devi essere almeno un moderatore. Agli articoli, oltre a titolo e testo, sono associati dei tag, che permettono di raggrupparli per argomenti simili e per favorire le funzioni di ricerca. Inoltre, gli articoli possono appartenere a una categoria.</p>
				</article>
				<article id='postInteractionArticle'>
					<h2>Interazione</h2>
					<p>L'interazione con gli articoli è facile e divertente! Puoi mettere "mi piace" o "non mi piace" con i grandi pulsanti dedicati, oppure commentare, in modo da confrontarti anche con gli altri utenti del nostro servizio. Abbiamo reso l'interfaccia utente il più amichevole possibile, per rendere facile e immediata l'esperienza di utilizzo.</p>
					<p>Per interagire con il servizio e con gli altri utenti, devi essere registrato e aver eseguito il login.</p>
					<p>E' possibile interagire anche con i commenti di altri utenti tramite "mi piace" e "non mi piace". Purtroppo non tutti gli utenti rispettano le regole di civiltà e convivenza, quindi abbiamo inserito la possibilità di segnalare un commento inappropriato o offensivo. I moderatori si occuperanno di prendere provvedimenti.</p>
				</article>
				<article id='controlPanelArticle'>
					<h2>Pannello di controllo</h2>
					<p>Il pannello di controllo è il centro della gestione del servizio. Per poterlo visualizzare devi essere un moderatore o un amministratore.</p>
					<p>Nel pannello di controllo è possibile tenere sotto controllo la situazione del servizio. E' possibile visualizzare tutti gli articoli e i commenti pubblicati, gestire la moderazione dei commenti, pubblicare nuovi articoli, visualizzare gli utenti registrati e modificarne i ruoli, aggiungere, rimuovere e modificare le categorie, e infine cambiare i dati del servizio stesso.</p>
				</article>
				<article id='panelPostsArticle'>
					<h2>Gestione degli articoli</h2>
					<p>Nel pannello di controllo puoi visualizzare gli articoli pubblicati e pubblicarne nuovi.</p>
					<p>In particolare, è possibile visualizzare tutti gli articoli e quelli pubblicati da te. Per ogni articolo, vengono visualizzati in forma tabulare i dettagli più importanti (titolo, testo, autore, data di pubblicazione, numero di "mi piace" e "non mi piace" e i tag associati). Hai pieno controllo sui tuoi articoli: li puoi modificare e eliminare a piacimento. Questi privilegi non sono disponibili per articoli di altri autori.</p>
					<p>Con i controlli in alto, puoi ordinare gli articoli in base a vari parametri. Gli articoli sono visualizzati 20 alla volta, e potrai visualizzare i successivi cliccando il pulsante dedicato.</p>
				</article>
				<article id='panelEditorArticle'>
					<h2>Aggiunta di articoli</h2>
					<p>Il nostro servizio integra un basilare editor di testo, che ti permetterà di pubblicare nuovi articoli. Quando scrivi un nuovo articolo, oltre a specificare per esso il titolo e il testo, puoi anche associargli una categoria e dei tag.</p>
					<p>Se non sei sicuro di voler pubblicare subito l'articolo, o se sei di fretta ma vuoi comunque iniziare a scrivere, puoi scegliere di salvare l'articolo come bozza e proseguire il lavoro più tardi. Un articolo bozza non può essere visualizzato dagli utenti semplici, mentre i moderatori possono vederlo, ma non modificarlo.</p>
					<p>Mentre scrivi, l'apparenza finale dell'articolo viene visualizzata nella finestra di destra.</p>
					<p>L'editor integra alcuni pulsanti di utilità che permettono di rendere visivamente più ricco il tuo articolo. Puoi scrivere in grassetto, corsivo e sottolineato e aggiungere immagini, link e citazioni.</p>
					<p>Se il tuo articolo è molto lungo, puoi scegliere di permetterne la piena fruizione solo quando viene aperto, e lasciare in homepage solo un frammento iniziale: questo è possibile grazie al pulsante con i puntini di sospensione, che aggiunge un frammento di codice speciale che viene interpretato come separatore in fase di visualizzazione.</p>
				</article>
				<article id='panelCommentArticle'>
					<h2>Commenti</h2>
					<p>Nel pannello di controllo puoi visualizzare tutti i commenti inseriti, divisi in tre grandi gruppi: tutti i commenti, i commenti ai tuoi articoli, e i commenti in moderazione.</p>
					<p>Per i commenti in moderazione, per i soli moderatori e amministratori è prevista la possibilità di rimozione. Per eliminare un commento in moderazione,nel pannello di controllo clicca sul testo e poi su "Elimina".</p>
					<p>Come per gli articoli, sono previste funzioni di ordinamento e di visualizzazione successiva ai 20 commenti.</p>
				</article>
				<article id='panelUserArticle'>
					<h2>Utenti</h2>
					<p>Nel pannello di controllo puoi visualizzare tutti gli utenti. Puoi accedere al tuo profilo, alla lista di tutti gli utenti o al solo staff.</p>
					<p>Cliccando sul nome utente di un qualsiasi utente, puoi visualizzare il suo profilo. Cliccando sulla sua email, puoi metterti in contatto con lui.</p>
				</article>
				<article id='panelSettingArticle'>
					<h2>Impostazioni</h2>
					<p>Il pannello di controllo permette agli utenti autorizzati di modificare alcuni parametri.</p>
					<p>Solo per gli amministratori, è possibile cambiare il nome e la descrizione del servizio.</p>
					<p>I moderatori e gli amministratori possono modificare, aggiungere e eliminare le categorie.</p>
					<p>I moderatori e gli amministratori possono modificare il rango degli altri utenti, con alcune ovvie limitazioni. In particolare, un utente non può modificare il proprio rango o quello di utenti con rango maggiore o uguale al proprio. In breve, i moderatori possono modificare solo gli utenti semplici, mentre gli amministratori possono modificare chiunque.</p>
				</article>
				<article id='registerArticle'>
					<h2>Registrazione</h2>
					<p>Nella pagina di accesso è possibile registrarsi al nostro servizio. Dopo la registrazione sarai un utente, e potrai finalmente interagire con gli articoli e con gli utenti con i meccanismi illustrati in precedenza. Come utente, sarai caratterizzato dal tuo username: sceglilo con cura, non è possibile cambiarlo successivamente!</p>
				</article>
				<article id='loginArticle'>
					<h2>Login</h2>
					<p>Se sei già registrato, con il login ottieni tutti i privilegi degli utenti registrati: potrai interagire con articoli e altri utenti. Per il login sono necessari username e password: non dimenticarli!</p>
				</article>
				<article id='setupArticle'>
					<h2>Configurazione</h2>
					<p>Il nostro servizio prevede una funzionalità di configurazione iniziale, che permette di impostare il nome e la descrizione del blog quando il blog viene creato. La pagina di setup è visibile solo se non è ancora stato specificato un nome per il blog: una volta completata la configurazione, la pagina non è più accessibile. L'utente che si registra in fase di configurazione diventa automaticamente amministratore del servizio.</p>
